import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    username: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
    },
    password: {
        type: String,
        required: true
    },
    roleType: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Role"
    },
}, {timestamps: true});

export const User = mongoose.model("User", userSchema);